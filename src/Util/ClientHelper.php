    public function request($method, $uri = '', array $options = [])
    {
        $newOptions = [];
        foreach ($options as $k => $v) {
            if (!empty($v)) {
                $newOptions[$k] = $v;
            }
        }
        if ($this->useProxy) {
// 获取一个可用ip
// 请求
// 如果请求失败则移出可用ip，
// 并再获取一个，
// 重试一次
// 失败则移出 返回报错
            try {
                $ip = $this->getProxy();
                $newOptions['proxy'] ="{$ip['ip']}:{$ip['port']}";
                $response = $this->client->request($method, $uri, $newOptions);
            } catch (\Exception $e) {
                //若是成功ip则重试一次
                if(!$this->isSuccess($ip)){
                    $this->tryRemoveIp($ip);
                    $ip = $this->getProxy();
                }

                $newOptions['proxy'] ="{$ip['ip']}:{$ip['port']}";
                try {
                    $response = $this->client->request($method, $uri, $newOptions);
                } catch (\Exception $e) {
                    $this->tryRemoveIp($ip);
                    dump($e,$ip);
                    throw new \DomainException("请求异常");
                }

            }
        } else {
            $response = $this->client->request($method, $uri, $newOptions);
        }

        $this->response = $response;
        $content = $this->getContent($response);

        if (static::$debug) {
            $newOptionStr = var_export($newOptions, true);
            $logs = "method:\n {$method}\n uri:\n {$uri}\n newOptions:\n {$newOptionStr}\n content:\n{$content}\n";
            file_put_contents(storage_path('logs/req-' . date('YmdHis-') . uniqid() . ''), $logs);
        }

        return $content;
    }

